public class Cobra {
    private String cor;
    private double tamanho;
    private boolean temVeneno;
 
    public double getTamanho(){
        return tamanho;
    }
    public void setTamanho(double novoTamanho){
        tamanho = novoTamanho;
    }
    public String getCor(){
        return cor;
    }
    public void setCor(String novaCor){
        cor = novaCor;
    }
    public boolean getTemVeneno(){
        return temVeneno;
    }
    public void setTemVeneno(boolean fv){
        temVeneno = fv;
    
    }


}
